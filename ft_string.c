#include "push_swap.h"

int	ft_string(char *a)
{
	int	i;
	int	j;

	i = 0;	j = 0;

	while (a[j] != '\0')
	{
		while (i < j)
		{
			if (a[i] == a[j])
				return (0);
			i++;
		}
		i = 0;
		j++;
	}
	return (1);
}
			
