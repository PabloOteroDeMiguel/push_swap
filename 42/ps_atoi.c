#include "push_swap.h"

long int	ps_atoi(const char *str)
{
	int			i;
	long int	n;
	long int	c;

	i = 0;
	while ((str[i] == ' ') || ((str[i] > 8) && (str[i] < 14 )))
		i++;
	c = 1;
	if ((str[i] == '-') || (str[i] == '+'))
	{
		if (str[i] == '-')
			c = -1;
		i++;
	}
	n = 0;
	while ((str[i] >= '0') && (str[i] <= '9'))
	{
		n = (n * 10) + (str[i] - '0');
		i++;
	}
	n = n * c;
	return (n);
}
