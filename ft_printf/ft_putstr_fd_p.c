#include "ft_printf.h"

int	ft_putstr_fd_p(char *s, int fd)
{
	int	i;

	i = 0;
	if (s == 0)
	{
		write(1, "(null)", 6);
		return (6);
	}
	while (s[i] != '\0')
	{
		ft_putchar_fd_p(s[i], fd);
		i++;
	}
	return (i);
}
